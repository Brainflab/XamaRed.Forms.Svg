namespace XamaRed.Forms.Svg
{
    /// <summary>
    /// Describes the position of the SVG from its parent canvas
    /// </summary>
    public enum SvgAlignment
    {
        /// <summary>
        /// The SVG will be aligned at the start (left or top) of its canvas, if applicable
        /// </summary>
        Start,

        /// <summary>
        /// The SVG will be aligned at the middle of its canvas, if applicable
        /// </summary>
        Middle,

        /// <summary>
        /// The SVG will be aligned at the end (right or bottom) of its canvas, if applicable
        /// </summary>
        End
    }
}
